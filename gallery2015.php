<script>
    // JavaScript to update modal content when an image is clicked
    const images = document.querySelectorAll('.service-img img');
    const modalImage = document.getElementById('modalImage');
    let currentIndex = 0;

    // Function to update modal content
    function updateModal(index) {
        modalImage.src = images[index].src;
        currentIndex = index;
    }

    // Event listener for previous button
    document.getElementById('prevButton').addEventListener('click', function() {
        if (currentIndex === 0) {
            updateModal(images.length - 1);
        } else {
            updateModal(currentIndex - 1);
        }
    });

    // Event listener for next button
    document.getElementById('nextButton').addEventListener('click', function() {
        if (currentIndex === images.length - 1) {
            updateModal(0);
        } else {
            updateModal(currentIndex + 1);
        }
    });

    // Event listeners for each image to update modal content
    images.forEach((img, index) => {
        img.addEventListener('click', function() {
            updateModal(index);
        });
    });
</script>
<script>
  // Get all pagination links
const paginationLinks = document.querySelectorAll('.pagination .page-item');

// Get all content areas
const contentAreas = document.querySelectorAll('.service-content');

// Event listener for pagination links
paginationLinks.forEach(link => {
    link.addEventListener('click', function(event) {
        event.preventDefault();

        // Remove 'active' class from all pagination links
        paginationLinks.forEach(link => {
            link.classList.remove('active');
        });

        // Add 'active' class to the clicked pagination link
        this.classList.add('active');

        // Hide all content areas
        contentAreas.forEach(area => {
            area.style.display = 'none';
        });

        // Get the page number from the clicked link
        const pageNumber = parseInt(this.textContent);

        // Show the content area corresponding to the clicked page
        const contentArea = document.getElementById(`page-content-${pageNumber}`);
        contentArea.style.display = 'block';
    });
});

</script>
